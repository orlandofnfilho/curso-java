package cap028;

public class Principal {

	public static void main(String[] args) {

		double x = 3;
		double y = 5;

		double a, b;

		a = Math.pow(x, 2);
		b = Math.sqrt(5);
		System.out.println(a);
		System.out.println(b);

		a = Math.abs(-y);
		b = Math.max(x, y);
		System.out.println(a);
		System.out.println(b);

	}

}
